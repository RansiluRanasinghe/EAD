    private void addToPayBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToPayBtn1ActionPerformed
        if (coursesCmbBox.getSelectedItem().equals("Course List")) {
            JOptionPane.showMessageDialog(null, "Please Select a course");
        } else {

            String tblCourseName = "";

            DefaultTableModel df = (DefaultTableModel) cartTable.getModel();
            int rowC = df.getRowCount();
            for (int i = 0; i < 10; i++) {
                tblCourseName = df.getValueAt(i, 0).toString();
            }
            if (coursesCmbBox.getSelectedItem().toString().equals(tblCourseName)) {
                JOptionPane.showMessageDialog(null, "Already in the cart");
            } else {
                String[] data = {coursesCmbBox.getSelectedItem().toString(), feeTxtBox.getText()};
                df.addRow(data);
                
                setTotal();

            }
        }
    }//GEN-LAST:event_addToPayBtn1ActionPerformed

