    public void loadCourses() {

        try {
            String getCourse = "select * from course";
            ResultSet rs = MyConn.search(getCourse);
            coursesCmbBox.addItem("Course List");

            while (rs.next()) {
                coursesCmbBox.addItem(rs.getString("CourseTitle"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void setTotal() {
        DefaultTableModel df = (DefaultTableModel) cartTable.getModel();
        int rowC = df.getRowCount();
        double total = 0;
        for (int i = 0; i < rowC; i++) {
            total += Double.parseDouble(df.getValueAt(i, 1).toString());
            subTotalTxtBox.setText(String.valueOf(total));
        }
    }

