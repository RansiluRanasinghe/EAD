    private void saveCourseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveCourseBtnActionPerformed
        try {
            if (courseIDTxtBox.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Please enter course ID");
                courseIDTxtBox.grabFocus();
            } else if (courseTitleTxtBox.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Please enter course title");
                courseTitleTxtBox.grabFocus();
            } else if (courseTitleTxtBox.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Please enter course title");
                courseTitleTxtBox.grabFocus();

            } else {
                JFrame f = new JFrame();
                if (JOptionPane.showConfirmDialog(f, "Are all the deatils correcr?") == JOptionPane.YES_NO_OPTION) {

                    String needToPay = "";
                    if (yearlyradioBtn.isSelected()) {
                        needToPay = "Yearly";
                    } else if (monthlyRadioBtn.isSelected()) {
                        needToPay = "Monthly";
                    }
                    String id = "STD-12345";
                    String saveCourse = "Insert into course(CourseID, CourseTitle, Duration, Fee, NeedToPay, student_StudentID) values ('" + courseIDTxtBox.getText() + "', '" + courseTitleTxtBox.getText() + "', '" + courseFeeTxtBox.getText() + "', '"+durationTxtBox2.getText()+"', '" + needToPay + "', '"+id+"')";
                    MyConn.save(saveCourse);
                    JOptionPane.showMessageDialog(null, "Save Successful");
                    clearFields();
                    idIncrement();
                } else {
                    JOptionPane.showMessageDialog(null, "Save Unsuccessful");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_saveCourseBtnActionPerformed

