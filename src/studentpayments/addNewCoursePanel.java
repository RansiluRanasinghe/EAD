    public void idIncrement(){
    
        int id = 1;
        try {
            String getId = "Select * from course";
            ResultSet rs = MyConn.search(getId);
            while (rs.next()) {                
                id = Integer.parseInt(rs.getString("CourseID"));
            }
            id++;
            courseIDTxtBox.setText(String.valueOf(id));
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        courseIDTxtBox.setEditable(false);
        
    } 

